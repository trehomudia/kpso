unsigned int Fact(const unsigned int n){
	static unsigned int f[10] = {1, 1, 2, 6, 24, 120, 720, 5040, 40320, 362880};
	if(n < 10)
		return f[n];
	 
	return(Fact(n-1)*n);
}
